using FACCTS.Server.Model.Enums;
using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.ComponentModel.DataAnnotations.Schema;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace FACCTS.Server.Model.DataModel
{
    [Table("Interpreters")]
    public partial class Interpreter : BaseEntity
    {

        public FACCTSEntity EntityType { get; set; } 

        public virtual CourtParty InterpreterFor { get; set; }

        [StringLength(255)]
        public string FirstName { get; set; }

        [StringLength(255)]
        public string LastName { get; set; }

        [StringLength(150)]
        public string Language { get; set; }

        [InverseProperty("Interpreters")]
        public virtual CaseRecord CaseRecord { get; set; }

        [NotMapped]
        public override ObjectState State
        {
            get
            {
                return base.State;
            }
            set
            {
                base.State = value;
            }
        }
    }
}
